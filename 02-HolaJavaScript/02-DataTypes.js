// Tipos de datos: diferentes tipos de valores en JavaScript

let text = "Hola";     // String
let number = 123;       // Number
let isActive = true;    // Boolean
let emptyValue = null;  // Null
let notDefined;         // Undefined

console.log(text, "es del tipo:", typeof text);
console.log(number, "es del tipo:", typeof number);
console.log(isActive, "es del tipo:", typeof isActive);
console.log(emptyValue, "es del tipo:", typeof emptyValue);
console.log(notDefined, "es del tipo:", typeof notDefined);
